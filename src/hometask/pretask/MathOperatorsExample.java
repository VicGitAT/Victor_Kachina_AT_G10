package hometask.pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int variableOne = 200;
        int variableTwo = 10;
        System.out.println("sum " + variableOne + " and " + variableTwo + " = " + (variableOne + variableTwo));
        System.out.println("subtraction " + variableOne + " and " + variableTwo + " = " + (variableOne - variableTwo));
        System.out.println("multiplication " + variableOne + " and " + variableTwo + " = " + (variableOne * variableTwo));
        System.out.println("division " + variableOne + " and " + variableTwo + " = " + (variableOne / variableTwo));
    }
}
