package hometask.pretask;

public class ConditionalExample {
    public static void main(String[] args) {
        int variableOne = 12;
        int variableForComparison = 10;
        if (variableOne > variableForComparison){
            System.out.println(variableOne + " > " + variableForComparison);
        } else {
            System.out.println(variableOne + " < " + variableForComparison);
        }
    }
}
