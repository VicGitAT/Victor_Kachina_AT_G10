package main.java.hometask.pretask;

public class MultiplicationTable {

    public static void main(String[] args) {
        int variableForMultiplicationTable = 5;
        for (int i = 1; i < 11; i++) {
            System.out.println(variableForMultiplicationTable + " * " + i + " = " + (variableForMultiplicationTable * i));
        }
    }
}
